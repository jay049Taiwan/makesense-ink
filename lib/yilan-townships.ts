// 中文鄉鎮名 → slug 對照（用於把 Notion DB08 的「行政區域」對應到地圖鄉鎮）
const NAME_TO_SLUG: Record<string, string> = {};
for (const t of TOWNSHIPS) {
  NAME_TO_SLUG[t.name] = t.id;
  NAME_TO_SLUG[t.name.replace(/[鄉鎮市]$/, "")] = t.id; // 容錯：頭城/礁溪 也能對到
}

export function townshipSlugFromRegion(region: string | string[] | null | undefined): string | null {
  if (!region) return null;
  const list = Array.isArray(region) ? region : [region];
  for (const r of list) {
    const slug = NAME_TO_SLUG[r] || NAME_TO_SLUG[r?.trim?.() || ""];
    if (slug) return slug;
  }
  return null;
}

// 把觀點散播在鄉鎮 labelAt 周圍（沒真實座標時用的 fallback）
export function seedXY(township: Township, index: number, total: number): [number, number] {
  if (total <= 1) return township.labelAt;
  const angle = (index / total) * Math.PI * 2 - Math.PI / 2;
  const radius = total <= 4 ? 16 : 22;
  return [
    Math.round(township.labelAt[0] + Math.cos(angle) * radius),
    Math.round(township.labelAt[1] + Math.sin(angle) * radius),
  ];
}
