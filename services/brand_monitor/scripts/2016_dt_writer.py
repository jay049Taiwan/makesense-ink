def archive_all_children(page_id):
    """archive page 的所有現有 children blocks"""
    r = requests.get(f"https://api.notion.com/v1/blocks/{page_id}/children?page_size=100", headers=HEADERS)
    r.raise_for_status()
    blocks = r.json().get("results", [])
    for b in blocks:
        bid = b["id"]
        # skip database/child page blocks
        if b.get("type") in ("child_database", "child_page"):
            continue
        requests.delete(f"https://api.notion.com/v1/blocks/{bid}", headers=HEADERS)
        time.sleep(0.2)


def append_paragraphs(page_id, content):
    """把 content（用 \n\n 分段）轉成 paragraph blocks append"""
    paragraphs = [p.strip() for p in content.split("\n\n") if p.strip()]
    children = [
        {
            "object": "block",
            "type": "paragraph",
            "paragraph": {"rich_text": [{"type": "text", "text": {"content": p}}]},
        }
        for p in paragraphs
    ]
    # Notion 上限 100 block/批，我們的 << 100 直接送
    r = requests.patch(
        f"https://api.notion.com/v1/blocks/{page_id}/children",
        headers=HEADERS,
        json={"children": children},
    )
    return r.status_code == 200, r.text[:200] if r.status_code != 200 else ""


