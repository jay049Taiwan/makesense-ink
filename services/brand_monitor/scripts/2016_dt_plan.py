def main():
    for pid, label, plan in PLANS:
        body = {
            "properties": {
                "ai企劃": {"status": {"name": "完成"}},
                "ai企劃備註": {"rich_text": [{"type": "text", "text": {"content": plan}}]},
            }
        }
        r = requests.patch(f"https://api.notion.com/v1/pages/{pid}", headers=HEADERS, json=body)
        status = "✅" if r.status_code == 200 else f"❌ {r.status_code}"
        chars = len(plan)
        print(f"  {status} {label} ({chars} 字)")
        if r.status_code != 200:
            print(f"    {r.text[:200]}")
        time.sleep(0.34)
    print("\n✨ 全部完成")


if __name__ == "__main__":
    main()
